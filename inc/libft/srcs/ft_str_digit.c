#include "libft.h"

int	ft_str_digit(char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if ((s[i] == '+' || s[i] == '-'))
			i++;
		if (!ft_isdigit(s[i]))
			return (0);
		i++;
	}
	return (1);
}
