#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_stack	*a;
	t_stack *b;

	b = 0;
	if (argc >= 2)
	{
		set_stack(&a, argv + 1, argc - 1);
		algorithm(&a, &b);
		free(a);
	}
	else
		ft_error(1);
	return (0);
}
